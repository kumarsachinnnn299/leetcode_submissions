//User function Template for C++
class Solution
{
public:
    int trailingZeroes(int N)
    {
        int temp=5;
        int ans=0;
        while(temp<=N)
        {
            ans+=(N/temp);
            temp*=5;
        }
        return ans;
    }
};

//{ Driver Code Starts.
int main()
{
    int t;
    cin >> t;
    while (t--)
    {
        int N;
        cin >> N;
        Solution ob;
        int ans  = ob.trailingZeroes(N);
        cout<<ans<<endl;
    }
    return 0;
}
